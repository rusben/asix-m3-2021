package net.xeill.elpuig;

class Calculator {

  public int sum(int a, int b) {
    return a+b;
  }

  public int substract(int a, int b) {
    return a-b;
  }

  public int multiply(int a, int b) {
    return a*b;
  }

  public int divide(int a, int b) {
    return a/b;
  }

  public int modulo(int a, int b) {
    return a%b;
  }

}
